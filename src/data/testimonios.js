export const testimoniosActivos = testimonios
  .filter((testimonio) => testimonio.activo)
  .sort((a, b) => a.orden - b.orden)

function obtenerTipoVisual(testimonio) {
  if (testimonio.videoUrl) return 'video'
  if (testimonio.imagenUrl) return 'imagen'

  return 'texto'
}

function ordenarPorPrioridadHome(testimonios) {
  return [...testimonios].sort((a, b) => {
    if (a.destacadoHome !== b.destacadoHome) {
      return a.destacadoHome ? -1 : 1
    }

    return a.orden - b.orden
  })
}

function seleccionarTestimoniosHome() {
  const colas = {
    video: ordenarPorPrioridadHome(
      testimoniosActivos.filter(
        (testimonio) =>
          obtenerTipoVisual(testimonio) === 'video'
      )
    ),

    imagen: ordenarPorPrioridadHome(
      testimoniosActivos.filter(
        (testimonio) =>
          obtenerTipoVisual(testimonio) === 'imagen'
      )
    ),

    texto: ordenarPorPrioridadHome(
      testimoniosActivos.filter(
        (testimonio) =>
          obtenerTipoVisual(testimonio) === 'texto'
      )
    ),
  }

  const patrones = [
    ['video', 'imagen', 'texto'],
    ['imagen', 'video', 'texto'],
    ['texto', 'video', 'imagen'],
    ['imagen', 'texto', 'video'],
    ['video', 'texto', 'imagen'],
    ['texto', 'imagen', 'video'],
  ]

  const resultado = []
  let indicePatron = 0

  function quedanTestimonios() {
    return (
      colas.video.length > 0 ||
      colas.imagen.length > 0 ||
      colas.texto.length > 0
    )
  }

  while (quedanTestimonios()) {
    const patron =
      patrones[indicePatron % patrones.length]

    patron.forEach((tipo) => {
      const testimonio = colas[tipo].shift()

      if (testimonio) {
        resultado.push(testimonio)
      }
    })

    if (resultado.length % 3 !== 0) {
      const tiposRestantes = [
        'video',
        'imagen',
        'texto',
      ]

      tiposRestantes.forEach((tipo) => {
        while (
          resultado.length % 3 !== 0 &&
          colas[tipo].length > 0
        ) {
          resultado.push(colas[tipo].shift())
        }
      })
    }

    indicePatron += 1
  }

  return resultado
}

export const testimoniosDestacados =
  seleccionarTestimoniosHome()

export function obtenerTestimonios({
  categoria,
  tipo,
  destacados,
  limite,
} = {}) {
  let resultado = [...testimoniosActivos]

  if (categoria) {
    resultado = resultado.filter(
      (testimonio) => testimonio.categoriaInterna === categoria
    )
  }

  if (tipo) {
    resultado = resultado.filter(
      (testimonio) => testimonio.tipo === tipo
    )
  }

  if (destacados) {
    resultado = resultado.filter(
      (testimonio) => testimonio.destacadoPagina
    )
  }

  if (limite) {
    resultado = resultado.slice(0, limite)
  }

  return resultado
}
